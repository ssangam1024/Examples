package main

import "fmt"

func main() {
	var i = true  // assigning value to varable i as true
	var j = false // assigning value to varable j as false

	fmt.Printf("%t\n", i) //this function printf() first formats its argument with formating verb given %t and then prints given i value in boolean ie., true or false and /n prints argument in new line.
	fmt.Printf("%t\n", j) //this function printf() first formats its argument with formating verb given %t and then prints given j value in boolean ie., true or false and /n prints argument in new line.
}
